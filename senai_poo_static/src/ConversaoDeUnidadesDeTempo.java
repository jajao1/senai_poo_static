public class ConversaoDeUnidadesDeTempo
{
    public static float minutosParaSegundos(float min) {  return min * 60;  }

    public static float horasParaMinutos(float horas) {  return horas * 60;  }

    public static float diasParaHoras(float dias) {  return dias * 24;  }

    public static float semanasParaDias(float semanas) {  return semanas * 7;  }

    public static float mesesParaDias(float mes) {  return mes * 30;  }

    public static float anosParaDias(float anos) {  return anos * 365.25f;  }
}
